#include<bits/stdc++.h>
using namespace std;

int c[5003], f[502], h[11];

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int n, k;

    cin >> n >> k;

    for(i = 1; i < n*k; i++)
        cin >> c[i];
    for(i = 1; i <= n; i++)
        cin >> f[i];
    for(i = 1; i <= k; i++)
        cin >> h[i];


}
